//
// TIM1共有四个通道，分别是：
//     通道1 ―― PA8 ┬─ 编码器
//     通道2 ―― PA9 ┘
//     通道3 ―― PA10
//     通道4 ―― PA11
//
// 注：一个定时器只能连接一个编码器
//
//////////////////////////////////////////////////////////////////////////////

#include "stm32f10x.h"

/**
  * @brief 旋转编码器初始化
  * @return 无
  */
void TIM1_Encoder_Init(void);

/**
  * @brief 获取当前旋转编码器的值, 并清零
  * @return 旋转编码器的值
  */
int16_t TIM1_Encoder_GetClear(void);

/**
  * @brief 获取当前旋转编码器的值
  * @return 旋转编码器的值
  */
uint32_t TIM1_Encoder_Get(void);

#endif
